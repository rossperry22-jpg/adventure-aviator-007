const fs = require('fs');
const path = require('path');

// Load existing questions from the file (it's not valid JSON, but we can parse as JS)
const existingPath = path.join(__dirname, 'existing_questions.json');
let existingContent = fs.readFileSync(existingPath, 'utf8');

// The file is a JavaScript array of objects without wrapping brackets.
// Let's try to evaluate as JavaScript by wrapping in array and fixing trailing commas.
// Since we cannot safely eval, we'll parse line by line.
// Instead, we'll use the fixed questions.js which now exports QUESTION_BANK.
const questionsPath = path.join(__dirname, 'scripts/questions.js');
const questionsModule = require(questionsPath);
const existingQuestions = questionsModule; // assuming default export

console.log(`Loaded ${existingQuestions.length} existing questions`);

// Define modules 00-07
const modules = ['00', '01', '02', '03', '04', '05', '06', '07'];
const questionsPerModule = 42;
const totalQuestions = 336;

let questionBank = [];

// For each module, generate 42 questions
let nextId = 0;
for (const module of modules) {
    // Determine which existing questions belong to this module (if any)
    const moduleExisting = existingQuestions.filter(q => q.module === module);
    const remaining = questionsPerModule - moduleExisting.length;
    
    // Add existing questions for this module (adjust IDs)
    for (const q of moduleExisting) {
        const newQuestion = { ...q, id: nextId++ };
        questionBank.push(newQuestion);
    }
    
    // Generate placeholder questions for the rest
    for (let i = 0; i < remaining; i++) {
        const placeholderId = nextId++;
        // Pick a random existing question as template
        const template = existingQuestions[Math.floor(Math.random() * existingQuestions.length)];
        const newQuestion = {
            id: placeholderId,
            module: module,
            question: `[PLACEHOLDER] Aviation knowledge question for Module ${module}, Question ${i + 1}`,
            options: [
                "Option A (correct)",
                "Option B",
                "Option C",
                "Option D"
            ],
            correct: 0,
            explanation: "This is a placeholder question. Real content will be generated by subject matter experts.",
            category: template.category,
            difficulty: ['Easy', 'Medium', 'Hard'][Math.floor(Math.random() * 3)],
            faaReference: template.faaReference,
            tags: [...template.tags, 'placeholder']
        };
        questionBank.push(newQuestion);
    }
}

// Ensure we have exactly 336 questions
if (questionBank.length !== totalQuestions) {
    console.error(`Generated ${questionBank.length} questions, expected ${totalQuestions}. Adjusting.`);
    // Add or remove to match
    if (questionBank.length < totalQuestions) {
        const missing = totalQuestions - questionBank.length;
        for (let i = 0; i < missing; i++) {
            questionBank.push({
                id: questionBank.length,
                module: '00',
                question: `Additional placeholder question ${i + 1}`,
                options: ["Correct", "Incorrect", "Incorrect", "Incorrect"],
                correct: 0,
                explanation: "Placeholder.",
                category: "General",
                difficulty: "Easy",
                faaReference: "PHAK Ch. 1",
                tags: ["placeholder"]
            });
        }
    } else {
        questionBank = questionBank.slice(0, totalQuestions);
    }
}

// Write to modules/question-bank.json
const modulesDir = path.join(__dirname, 'modules');
if (!fs.existsSync(modulesDir)) {
    fs.mkdirSync(modulesDir, { recursive: true });
}
const outputPath = path.join(modulesDir, 'question-bank.json');
fs.writeFileSync(outputPath, JSON.stringify(questionBank, null, 2));
console.log(`Generated ${questionBank.length} questions in ${outputPath}`);

// Also update scripts/questions.js with the new question bank
const updatedQuestionsJS = `// Adventure Aviator Question Bank
// Generated on ${new Date().toISOString()}

const QUESTION_BANK = ${JSON.stringify(questionBank, null, 2)};

if (typeof module !== 'undefined' && module.exports) {
    module.exports = QUESTION_BANK;
}

// For browser global
if (typeof window !== 'undefined') {
    window.QUESTION_BANK = QUESTION_BANK;
}`;

fs.writeFileSync(path.join(__dirname, 'scripts/questions.js'), updatedQuestionsJS);
console.log('Updated scripts/questions.js');